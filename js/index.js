$(document).ready( function() {
    $('#shorten').click(function(event) {
        event.preventDefault();
        if(!isUrlValid($("#urladdress").val())) {
            Swal.fire({
                icon: 'error',
                title: 'Oops...',
                text: 'Please input a valid url',
                showConfirmButton: false,
                timer: 2500
            });
            $("#urladdress").addClass("is-invalid");
        } else {
            sendUrl();
        }
    });

    $("#urladdress").on("focus", function(e){
        if($(this).hasClass("is-invalid")){
            $(this).removeClass("is-invalid");
        }
    });
});

